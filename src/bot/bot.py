            logger.info("=== –ó–∞–≤–µ—Ä—à–µ–Ω–∏–µ –æ–±—Ä–∞–±–æ—Ç–∫–∏ callback-–∑–∞–ø—Ä–æ—Å–∞ –Ω–∞ –º–æ–¥–µ—Ä–∞—Ü–∏—é ===")
            
